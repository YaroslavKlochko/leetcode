package algorithms.dataStructures;

public class ListNode {
    public int value;
    public ListNode next;
    public int size;

    public ListNode() {
    }

    public ListNode(int value, ListNode next) {
        this.value = value;
        this.next = next;
    }

    public ListNode(int value) {
        this.value = value;
    }

    public int size() {
        return size;
    }

    public int[] get(int current) {
        return new int[1];
    }
}
